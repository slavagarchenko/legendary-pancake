ttl.forward(75)
ttl.right(90)
ttl.forward(150)
ttl.end_fill()

#trapezoid
ttl.right(90)
ttl.forward(75)#точка границы с прямоугольником
ttl.fillcolor('blue')
ttl.begin_fill()
ttl.forward(140)#левая грань трапеции
ttl.right(90)
ttl.forward(99)
ttl.right(70)
ttl.forward(149)
ttl.end_fill()
turtle.done()

